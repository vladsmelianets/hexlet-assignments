package exercise;

// BEGIN
public final class Point {
    private final int x;
    private final int y;

    public Point(int xVal, int yVal) {
        this.x = xVal;
        this.y = yVal;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}

